function load() {
  let fontFaces = '';

  names.forEach(name => {
    const fontFace = `@font-face {
      font-family: '${name}';
      src: url('TTFs/${name}.ttf') format('truetype');
      font-weight: normal;
      font-style: normal;
    }
    .${name} {
      font-family: '${name}';
    }`;

    fontFaces += fontFace;
  });

  const style = document.createElement('style');
  style.type = 'text/css';
  style.appendChild(document.createTextNode(fontFaces));
  document.head.appendChild(style);

  names.forEach(name => {
    var p = document.createElement("p");
    p.innerHTML = name;
    p.setAttribute("style-Regular", `font-family: "${name}";`); // Added font-style here
    document.body.appendChild(p);
  });
}
